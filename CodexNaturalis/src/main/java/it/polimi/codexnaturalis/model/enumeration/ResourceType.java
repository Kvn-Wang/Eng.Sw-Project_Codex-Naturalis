package it.polimi.codexnaturalis.model.enumeration;

/**
 * The enum Resource type.
 */
public enum ResourceType {
    /**
     * You can't place the card
     */
    UNASSIGNABLE,
    /**
     * Free space where you can place the card
     */
    NONE,
    /**
     * Plant resource type.
     */
    PLANT,
    /**
     * Animal resource type.
     */
    ANIMAL,
    /**
     * Fungi resource type.
     */
    FUNGI,
    /**
     * Insect resource type.
     */
    INSECT,
    /**
     * Quill resource type.
     */
    QUILL,
    /**
     * Inkwell resource type.
     */
    INKWELL,
    /**
     * Manuscript resource type.
     */
    MANUSCRIPT
}
